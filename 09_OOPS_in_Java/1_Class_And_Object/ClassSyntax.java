package cao;

// Definition of the class
// class is collection of data member and member function.
/* class can have following attributes:
    - instance or static variable
    - instance method
    - static method
    - instance block
    - static block
*/

/*
<access-specifier> class
*/
public class ClassSyntax{
        // Instance variable
        int instanceVariable = 10;

        // Static variable
        static int var = 10;

        // Instance block
        {
            // code to be executed.
        }

        // Static Block.
        static{
            // code to be executed.
        }

        // Instance method
        public void instanceMethod(){
            // code to be executed.
        } 

        // Static Method
        public static void staticMethod(){
            // code to be executed.
        }
};