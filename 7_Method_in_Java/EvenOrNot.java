package method;

public class EvenOrNot{
    public void checkEvenOrOdd(int num){
        if(num%2==0){
            System.out.println("Number is Even.");
        }
        else{
            System.out.println("Number is Odd.");
        }
    }

    public void checkEvenOrNot(int num){
        if(num%2 == 0){
            System.out.println("Number is Even.");
        }
        else{
            System.out.println("Number is not even.");
        }
    }
};